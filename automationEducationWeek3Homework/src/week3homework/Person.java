package week3homework;

public class Person {

    String name;
    String surname;
    int age;
    String gender;

    public Person(){
        name = "Ayşe";
        surname = "Yürek";
        age = 25;
        gender = "Kadın";
    }

    public Person(String name, String surname, int age, String gender){
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.gender = gender;
    }

}


